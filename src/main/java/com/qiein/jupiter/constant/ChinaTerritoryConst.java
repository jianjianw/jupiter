    /**
     * 初始化地域map
     */
    private static void init() {
        System.out.println(PROVINCE_LIST);
        StringBuffer sb = null;
        for (TerritoryDTO t : PROVINCE_LIST) {
            sb = new StringBuffer();
            //省份
            if (StringUtil.isNotEmpty(t.getProRemark()) && (t.getProRemark().equals(SF) || t.getProRemark().equals(ZXS))) {
                for (TerritoryDTO territoryDTO : getCityListByProId(t.getProId())) {
                    sb.append("," + territoryDTO.getName().substring(0, territoryDTO.getName().length() - 1));
                }
                TERRITORY_MAP.put(t.getName().substring(0, t.getName().length() - 1), sb.toString().substring(1));
            } else if (StringUtil.isNotEmpty(t.getProRemark()) && t.getProRemark().equals(ZZQ)) {
                for (TerritoryDTO territoryDTO : getCityListByProId(t.getProId())) {
                    sb.append("," + territoryDTO.getName().substring(0, territoryDTO.getName().length() - 1));
                }
                TERRITORY_MAP.put(t.getName().substring(0, t.getName().length() - ZZQ.length()), sb.toString().substring(1));
            } else if (StringUtil.isNotEmpty(t.getProRemark()) && t.getProRemark().equals(TBXZQ)) {
                for (TerritoryDTO territoryDTO : getCityListByProId(t.getProId())) {
                    sb.append("," + territoryDTO.getName().substring(0, territoryDTO.getName().length() - TBXZQ.length()));
                }
                TERRITORY_MAP.put(t.getName().substring(0, t.getName().length() - TBXZQ.length()), sb.toString().substring(1));
            }
        }
    }

    private static List<TerritoryDTO> getCityListByProId(Integer proId) {
        List<TerritoryDTO> list = new ArrayList<>();
        for (TerritoryDTO territoryDTO : CITY_LIST)
            if (territoryDTO.getProId() == proId)
                list.add(territoryDTO);
        return list;
    }

